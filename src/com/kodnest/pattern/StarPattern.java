package com.kodnest.pattern;

public class StarPattern {
	public static void main(String[] args) {
		for(int i=0;i<=3;i++)
		{
			for(int j=0;j<=2;j++)
			{
				System.out.print("*");
			}
			System.out.println();
		}
			
		for(int l=0;l<=2;l++)
		{
			for(int k=0;k<=3;k++)
			{
				System.out.print("#");
			}
			System.out.println();
			
			
		}
		
	}

}
