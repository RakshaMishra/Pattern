package com.kodnest.pattern;

public class HomeWork2 {
	public static void main(String[] args) {
		for(int j=1;j<=3;j++)
		{
			for(int i=1;i<=3;i++)
			{
				System.out.print("*");
				
			}
			
			for(int k=1;k<=3;k++)
			{
				System.out.print("%");
				
			}
			for(int l=1;l<=3;l++)
			{
				System.out.print("$");
				
			}
			
			
			System.out.println();
		}
	}

}
