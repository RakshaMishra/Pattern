package com.kodnest.pattern;

public class StarPattern5 {
	public static void main(String[] args) {
		for(int j=1;j<=4;j++)
		{
			for(int i=1;i<=j;i++)
			{
				System.out.print("*");
			}
			System.out.println();
		}
		
		for(int k=1;k<=3;k++)
		{
			for(int l=1;l<=3;l++)
			{
				System.out.print("*");
			}
			System.out.println();
		}
		
		for(int m=1;m<=4;m++)
		{
			for(int n=1;n<=m;n++)
			{
				System.out.print("*");
			}
			System.out.println();
		}
		
	}

}
